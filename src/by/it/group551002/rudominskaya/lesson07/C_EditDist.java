import java.io.FileNotFoundException;
import java.io.InputStream;
import java.util.Scanner;

/*
Задача на программирование: расстояние Левенштейна
    https://ru.wikipedia.org/wiki/Расстояние_Левенштейна
    http://planetcalc.ru/1721/

Дано:
    Две данных непустые строки длины не более 100, содержащие строчные буквы латинского алфавита.

Необходимо:
    Решить задачу МЕТОДАМИ ДИНАМИЧЕСКОГО ПРОГРАММИРОВАНИЯ
    Итерационно вычислить алгоритм преобразования двух данных непустых строк
    Вывести через запятую редакционное предписание в формате:
     операция("+" вставка, "-" удаление, "~" замена, "#" копирование)
     символ замены или вставки

    Sample Input 1:
    ab
    ab
    Sample Output 1:
    #,#,

    Sample Input 2:
    short
    ports
    Sample Output 2:
    -s,~p,#,#,#,+s,

    Sample Input 3:
    distance
    editing
    Sample Output 3:
    +e,#,#,-s,#,~i,#,-c,~g,

    P.S. В литературе обычно действия редакционных предписаний обозначаются так:
    - D (англ. delete) — удалить,
    + I (англ. insert) — вставить,
    ~ R (replace) — заменить,
    # M (match) — совпадение.
*/

public class C_EditDist {


    String getDistanceEdinting(String one, String two) {


        //!!!!!!!!!!!!!!!!!!!!!!!!!     НАЧАЛО ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!
        
        // Получаем длины строк
        int m = one.length();
        int n = two.length();

        // Создаём таблицу  размером (m+1) x (n+1)
        // dp[i][j] = минимальное расстояние редактирования между первыми i символами
        // строки one и первыми j символами строки two
        int[][] dp = new int[m + 1][n + 1];
        

        // Инициализируем первый столбец
        for (int i = 0; i <= m; i++) {
            dp[i][0] = i;  // стоимость = количество удалений
        }
        
        // Инициализируем первую строку
        for (int j = 0; j <= n; j++) {
            dp[0][j] = j;  // стоимость = количество вставок
        }
        

        // Заполняем таблицу построчно, начиная с dp[1][1]
        for (int i = 1; i <= m; i++) {
            for (int j = 1; j <= n; j++) {

                // 0 - если символы совпадают (не нужно заменять)
                // 1 - если символы разные (нужна замена)
                int cost = (one.charAt(i - 1) == two.charAt(j - 1)) ? 0 : 1;
                
                // Вычисляем минимальную стоимость из трёх возможных операций:
                // 1. Удаление символа из первой строки: dp[i-1][j] + 1
                // 2. Вставка символа во вторую строку: dp[i][j-1] + 1
                // 3. Замена (или копирование) символа: dp[i-1][j-1] + cost
                dp[i][j] = Math.min(
                    Math.min(dp[i - 1][j] + 1,      // удаление
                             dp[i][j - 1] + 1),     // вставка
                    dp[i - 1][j - 1] + cost         // замена или копирование
                );
            }
        }
        

        // Идём обратным ходом от правого нижнего угла таблицы dp[m][n] к dp[0][0]
        // Восстанавливаем, какая операция была выполнена на каждом шаге
        
        StringBuilder result = new StringBuilder();  // Строка для результата
        int i = m;  // текущая позиция в первой строке
        int j = n;  // текущая позиция во второй строке
        
        // Пока не дошли до начала обеих строк
        while (i > 0 || j > 0) {
            

            // Если символы одинаковые, то лучшая операция - копирование
            // Стоимость операции: 0
            if (i > 0 && j > 0 && one.charAt(i - 1) == two.charAt(j - 1)) {

                // Добавляем операцию копирования в начало строки
                result.insert(0, "#,");
                i--;  // переходим к предыдущему символу первой строки
                j--;  // переходим к предыдущему символу второй строки
            }

            // Проверяем, что пришли в текущую клетку через замену
            else if (i > 0 && j > 0 && dp[i][j] == dp[i - 1][j - 1] + 1) {
                // Добавляем операцию замены в начало строки
                result.insert(0, "~" + two.charAt(j - 1) + ",");  // "~X" - заменить на X
                i--;  // переходим к предыдущему символу первой строки
                j--;  // переходим к предыдущему символу второй строки
            }

            // Проверяем, что пришли в текущую клетку через удаление
            else if (i > 0 && dp[i][j] == dp[i - 1][j] + 1) {
                // Добавляем операцию удаления в начало строки
                result.insert(0, "-" + one.charAt(i - 1) + ",");
                i--;  // переходим к предыдущему символу первой строки
            }

            // Проверяем, что пришли в текущую клетку через вставку
            else if (j > 0 && dp[i][j] == dp[i][j - 1] + 1) {
                // Добавляем операцию вставки в начало строки
                result.insert(0, "+" + two.charAt(j - 1) + ",");
                j--;  // переходим к предыдущему символу второй строки
            }
        }
        
        //!!!!!!!!!!!!!!!!!!!!!!!!!     КОНЕЦ ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!
        return result.toString();
    }


    public static void main(String[] args) throws FileNotFoundException {
        // Открываем файл с тестовыми данными
        InputStream stream = C_EditDist.class.getResourceAsStream("dataABC.txt");
        C_EditDist instance = new C_EditDist();
        Scanner scanner = new Scanner(stream);
        
        // Читаем три пары строк и выводим результат для каждой
        System.out.println(instance.getDistanceEdinting(scanner.nextLine(), scanner.nextLine()));
        System.out.println(instance.getDistanceEdinting(scanner.nextLine(), scanner.nextLine()));
        System.out.println(instance.getDistanceEdinting(scanner.nextLine(), scanner.nextLine()));
    }
}